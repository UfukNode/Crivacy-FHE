/**
 * Customer profile handlers — manage display name, phone, and avatar.
 *
 * Each exported function takes a `CustomerContext` (built by
 * `customerRoute`) and returns a `NextResponse`. The route files in
 * `app/api/customer/profile/` and `app/api/customer/avatar/` wire
 * these handlers through the customer middleware pipeline.
 *
 * Field mutability rules:
 *
 *   - `email`          — always locked (change requires separate flow)
 *   - `displayName`    — always editable
 *   - `phone`          — editable until kycFieldsLocked becomes true
 *   - `fullName`       — immutable (set by Didit, never editable)
 *   - `dateOfBirth`    — immutable
 *   - `nationality`    — immutable
 *   - `documentType`   — immutable
 *   - `documentCountry`— immutable
 *   - `addressLine`    — immutable
 *   - `addressCity`    — immutable
 *   - `addressCountry` — immutable
 *
 * @module
 */

import { NextResponse } from 'next/server';
import { and, eq, isNull, ne } from 'drizzle-orm';
import { z } from 'zod';
import { displayNameSchema, phoneSchema } from '@/lib/validation/profile';

import type { CustomerContext } from '../context';
import * as schema from '@/lib/db/schema';
import { SHARP_INPUT_GUARD } from '@/lib/image/sharp-limits';
import { writeAudit } from '@/lib/audit/writer';
import { customerActor, customerLabel } from '@/lib/audit/actors';
import { uuidTarget } from '@/lib/audit/targets';
import { buildRequestContext as buildAuditRequestContext } from '@/lib/audit/context';

// ---------------------------------------------------------------------------
// Constants
