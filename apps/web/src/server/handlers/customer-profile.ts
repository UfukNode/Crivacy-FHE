/**
 * Normalize a display name: NFC normalize, strip HTML tags, strip control
 * characters (except space/newline/tab which are also stripped for display
 * names).
 */
function normalizeDisplayName(raw: string): string {
  // NFC normalize Unicode
  let result = raw.normalize('NFC');
  // Strip HTML tags
  result = result.replace(/<[^>]*>/g, '');
  // Strip control characters (U+0000..U+001F, U+007F..U+009F)
  result = result.replace(/[\u0000-\u001f\u007f-\u009f]/g, '');
  // Collapse whitespace and trim
  result = result.replace(/\s+/g, ' ').trim();
  return result;
}

/**
 * Build the mutability map for profile fields based on the current
 * kycFieldsLocked state.
 */
function buildMutabilityMap(kycFieldsLocked: boolean): Record<string, string> {
  return {
    email: 'locked',
    displayName: 'editable',
    phone: kycFieldsLocked ? 'locked' : 'editable',
    fullName: 'immutable',
    dateOfBirth: 'immutable',
    nationality: 'immutable',
    documentType: 'immutable',
    documentCountry: 'immutable',
    addressLine: 'immutable',
    addressCity: 'immutable',
    addressCountry: 'immutable',
  };
}

/**
 * Get the base path for avatar file storage. Uses process.cwd() + /data/uploads/avatars/.
 */
function getAvatarBasePath(): string {
  return `${process.cwd()}/data/uploads/avatars`;
}

/** UUID format regex (non-v4-specific; storage keys are generated by crypto.randomUUID). */
const AVATAR_STORAGE_KEY_REGEX = /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i;

/**
 * Read an avatar WebP file from disk by its storage key.
 *
 * Returns `null` if the key is malformed or the file is missing.
 * The caller is responsible for producing the HTTP response (including
 * choosing the appropriate `Cache-Control` header for its audience).
 *
 * Used by both the customer-facing serve handler and the admin-facing
 * customer avatar serve handler, to keep file access logic in one place.
 */
export async function readAvatarFile(storageKey: string): Promise<Buffer | null> {
  if (!AVATAR_STORAGE_KEY_REGEX.test(storageKey)) {
    return null;
  }
  const { readFile } = await import('node:fs/promises');
  const path = await import('node:path');
  const basePath = getAvatarBasePath();
  const filePath = path.join(basePath, `${storageKey}.webp`);
  try {
    return await readFile(filePath);
  } catch {
    return null;
  }
}

/**
 * Validate magic bytes of the uploaded file buffer.
 * Returns true if the buffer starts with JPEG, PNG, or WebP magic bytes.
 */
function isAllowedImageFormat(buffer: Uint8Array): boolean {
  if (buffer.length < 12) return false;

  // JPEG: FFD8FF
  if (
    buffer[0] === JPEG_MAGIC[0] &&
    buffer[1] === JPEG_MAGIC[1] &&
    buffer[2] === JPEG_MAGIC[2]
  ) {
    return true;
  }

  // PNG: 89504E47
  if (
    buffer[0] === PNG_MAGIC[0] &&
    buffer[1] === PNG_MAGIC[1] &&
    buffer[2] === PNG_MAGIC[2] &&
    buffer[3] === PNG_MAGIC[3]
  ) {
    return true;
  }

  // WebP: RIFF....WEBP
  if (
    buffer[0] === RIFF_MAGIC[0] &&
    buffer[1] === RIFF_MAGIC[1] &&
    buffer[2] === RIFF_MAGIC[2] &&
    buffer[3] === RIFF_MAGIC[3] &&
    buffer[8] === WEBP_MAGIC[0] &&
    buffer[9] === WEBP_MAGIC[1] &&
    buffer[10] === WEBP_MAGIC[2] &&
    buffer[11] === WEBP_MAGIC[3]
  ) {
    return true;
  }

  return false;
}

