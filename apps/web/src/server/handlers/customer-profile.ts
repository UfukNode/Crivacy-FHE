/** Maximum avatar file size in bytes: 2 MiB. */
const MAX_AVATAR_BYTES = 2 * 1024 * 1024;

/** JPEG magic bytes (first 3 bytes). */
const JPEG_MAGIC = new Uint8Array([0xff, 0xd8, 0xff]);

/** PNG magic bytes (first 4 bytes). */
const PNG_MAGIC = new Uint8Array([0x89, 0x50, 0x4e, 0x47]);

/** WebP: starts with "RIFF" (4 bytes), then 4 bytes size, then "WEBP" (4 bytes). */
const RIFF_MAGIC = new Uint8Array([0x52, 0x49, 0x46, 0x46]);
const WEBP_MAGIC = new Uint8Array([0x57, 0x45, 0x42, 0x50]);

// ---------------------------------------------------------------------------
// Validation schema
// ---------------------------------------------------------------------------

const UpdateProfileSchema = z
  .object({
    displayName: displayNameSchema.optional(),
    phone: phoneSchema.optional(),
    onboardingDismissed: z.literal(true).optional(),
  })
  .strict();

