/**
 * DELETE /api/customer/avatar
 *
 * Remove the customer's avatar image. Deletes the file from disk and
 * clears the avatarStorageKey in the DB.
 */
export async function handleDeleteAvatar(ctx: CustomerContext): Promise<NextResponse> {
  const { customer, db, now } = ctx;

  // --- 1. Get current avatar key ---
  const rows = await db
    .select({ avatarStorageKey: schema.customers.avatarStorageKey })
    .from(schema.customers)
    .where(eq(schema.customers.id, customer.id))
    .limit(1);

  const row = rows[0];
  if (row === undefined || row.avatarStorageKey === null) {
    return ctx.errorJson('not_found', 'No avatar to delete.', 404);
  }

  // --- 2. Delete file ---
  const { unlink } = await import('node:fs/promises');
  const path = await import('node:path');
  const basePath = getAvatarBasePath();
  const filePath = path.join(basePath, `${row.avatarStorageKey}.webp`);

  try {
    await unlink(filePath);
  } catch {
    // File may already be deleted — proceed to clear DB
  }

  // --- 3. Clear DB ---
  await db
    .update(schema.customers)
    .set({ avatarStorageKey: null, updatedAt: now })
    .where(eq(schema.customers.id, customer.id));

  // --- 4. Audit ---
  const actor = customerActor({ id: customer.id, label: customerLabel(customer) });
  const target = uuidTarget({ kind: 'customer', id: customer.id });
  const auditCtx = buildAuditRequestContext({
    ip: ctx.ip,
    userAgent: ctx.userAgent,
    requestId: ctx.requestId,
  });

  await writeAudit(db, {
    action: 'customer.avatar_removed',
    actor,
    target,
    context: auditCtx,
    meta: { removedKey: row.avatarStorageKey },
    ts: now,
  });

  return ctx.noContent();
}

