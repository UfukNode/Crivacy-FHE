/**
 * GET /api/customer/profile
 *
 * Returns the full customer profile with mutability metadata for each field.
 */
export async function handleGetProfile(ctx: CustomerContext): Promise<NextResponse> {
  const { customer, db } = ctx;

  // Fetch the full customer row (the context only has a subset)
  // Crivacy stores ZERO raw PII columns post PII-purge migration
  // (20260509000000) — full_name / date_of_birth / nationality /
  // document_* / address_* are NOT selected because they no longer
  // exist on this table. The customer can view their underlying PII
  // by following the Didit deep-link surfaced elsewhere in the UI.
  const rows = await db
    .select({
      id: schema.customers.id,
      email: schema.customers.email,
      displayName: schema.customers.displayName,
      phone: schema.customers.phone,
      kycFieldsLocked: schema.customers.kycFieldsLocked,
      avatarStorageKey: schema.customers.avatarStorageKey,
      kycLevel: schema.customers.kycLevel,
      kycScore: schema.customers.kycScore,
      emailVerifiedAt: schema.customers.emailVerifiedAt,
      onboardingDismissedAt: schema.customers.onboardingDismissedAt,
      createdAt: schema.customers.createdAt,
      updatedAt: schema.customers.updatedAt,
    })
    .from(schema.customers)
    .where(eq(schema.customers.id, customer.id))
    .limit(1);

  const row = rows[0];
  if (row === undefined) {
    return ctx.errorJson('not_found', 'Customer profile not found.', 404);
  }

  const avatarUrl =
    row.avatarStorageKey !== null
      ? `/api/customer/avatar/${row.avatarStorageKey}`
      : null;

  return ctx.json({
    id: row.id,
    email: row.email,
    displayName: row.displayName,
    phone: row.phone,
    // No PII fields — Crivacy holds none. The customer can fetch
    // their underlying KYC data from Didit via the deep-link
    // surfaced on the verification card.
    kycLevel: row.kycLevel,
    kycScore: row.kycScore,
    avatarUrl,
    emailVerifiedAt: row.emailVerifiedAt !== null ? row.emailVerifiedAt.toISOString() : null,
    onboardingDismissedAt: row.onboardingDismissedAt !== null ? row.onboardingDismissedAt.toISOString() : null,
    createdAt: row.createdAt.toISOString(),
    updatedAt: row.updatedAt.toISOString(),
    mutability: buildMutabilityMap(row.kycFieldsLocked),
  });
}

