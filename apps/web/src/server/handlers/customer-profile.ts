/**
 * PATCH /api/customer/profile
 *
 * Update editable profile fields (displayName, phone). Rejects attempts
 * to modify immutable or locked fields.
 */
export async function handleUpdateProfile(ctx: CustomerContext): Promise<NextResponse> {
  const { customer, db, now, request } = ctx;

  // --- 1. Parse and validate body ---
  let body: z.infer<typeof UpdateProfileSchema>;
  try {
    const contentType = request.headers.get('content-type');
    if (contentType === null || !contentType.includes('application/json')) {
      return ctx.errorJson('unsupported_media_type', 'Content-Type must be application/json.', 415);
    }
    const raw = await request.json();
    body = UpdateProfileSchema.parse(raw);
  } catch (err) {
    if (err instanceof z.ZodError) {
      return ctx.errorJson('validation_failed', 'Invalid input.', 400, {
        issues: err.issues.map((i) => ({ path: i.path, message: i.message })),
      });
    }
    return ctx.errorJson('malformed_json', 'Request body is not valid JSON.', 400);
  }

  // Nothing to update
  if (body.displayName === undefined && body.phone === undefined && body.onboardingDismissed === undefined) {
    return ctx.errorJson('validation_failed', 'No fields to update.', 400);
  }

  // --- 2. Check kycFieldsLocked for phone ---
  if (body.phone !== undefined) {
    const lockRows = await db
      .select({ kycFieldsLocked: schema.customers.kycFieldsLocked })
      .from(schema.customers)
      .where(eq(schema.customers.id, customer.id))
      .limit(1);

    const lockRow = lockRows[0];
    if (lockRow !== undefined && lockRow.kycFieldsLocked) {
      return ctx.errorJson(
        'field_locked',
        'Phone number cannot be changed after KYC verification.',
        403,
      );
    }

    // Check phone uniqueness among active (non-deleted) customers
    const phoneConflict = await db
      .select({ id: schema.customers.id })
      .from(schema.customers)
      .where(
        and(
          eq(schema.customers.phone, body.phone),
          ne(schema.customers.id, customer.id),
          isNull(schema.customers.deletedAt),
        ),
      )
      .limit(1);

    if (phoneConflict.length > 0) {
      return ctx.errorJson(
        'conflict',
        'This phone number is already associated with another account.',
        409,
      );
    }
  }

  // --- 3. Prepare update values ---
  const updates: Record<string, unknown> = { updatedAt: now };
  const auditActions: Array<{ action: 'customer.profile_updated' | 'customer.phone_updated'; meta: Record<string, unknown> }> = [];

  if (body.displayName !== undefined) {
    const normalized = normalizeDisplayName(body.displayName);
    if (normalized.length < 2) {
      return ctx.errorJson(
        'validation_failed',
        'Display name must be at least 2 characters after normalization.',
        400,
      );
    }
    updates['displayName'] = normalized;
    auditActions.push({
      action: 'customer.profile_updated',
      meta: { field: 'displayName', newValue: normalized },
    });
  }

  if (body.phone !== undefined) {
    updates['phone'] = body.phone;
    auditActions.push({
      action: 'customer.phone_updated',
      meta: { field: 'phone' },
    });
  }

  if (body.onboardingDismissed === true) {
    updates['onboardingDismissedAt'] = now;
    auditActions.push({
      action: 'customer.profile_updated',
      meta: { field: 'onboardingDismissedAt' },
    });
  }

  // --- 4. Update ---
  await db
    .update(schema.customers)
    .set(updates)
    .where(eq(schema.customers.id, customer.id));

  // --- 5. Audit ---
  const actor = customerActor({
    id: customer.id,
    label: customerLabel(customer),
  });
  const target = uuidTarget({ kind: 'customer', id: customer.id });
  const auditCtx = buildAuditRequestContext({
    ip: ctx.ip,
    userAgent: ctx.userAgent,
    requestId: ctx.requestId,
  });

  for (const entry of auditActions) {
    await writeAudit(db, {
      action: entry.action,
      actor,
      target,
      context: auditCtx,
      meta: entry.meta,
      ts: now,
    });
  }

  return ctx.json({ updated: true });
}

