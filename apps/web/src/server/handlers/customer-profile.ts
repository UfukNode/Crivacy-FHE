/**
 * GET /api/customer/avatar/[uuid]
 *
 * Serve the avatar image for the given storage key. Returns the WebP
 * file with immutable cache headers. The route is public within the
 * customer auth context — only the authenticated customer's UUID will
 * resolve to a file.
 */
export async function handleServeAvatar(
  ctx: CustomerContext,
  uuid: string,
): Promise<NextResponse> {
  const fileBuffer = await readAvatarFile(uuid);
  if (fileBuffer === null) {
    return ctx.errorJson('not_found', 'Avatar not found.', 404);
  }

  return new NextResponse(fileBuffer, {
    status: 200,
    headers: {
      'Content-Type': 'image/webp',
      // AUD-CUS-CACHE-001 fix: `private` so a CDN between client and
      // origin cannot cache this auth-gated response with a URL-only
      // key. Without this, the first authenticated customer to hit
      // `/api/customer/avatar/<uuid>` would populate the shared
      // cache, and any later unauthenticated request to the same URL
      // (the UUID is unguessable but can leak via logs, ticket
      // attachments, etc.) would bypass the customerRoute auth gate
      // entirely. `private` keeps the browser's per-user cache alive
      // while forbidding shared-cache storage.
      'Cache-Control': 'private, max-age=86400, immutable',
      'Content-Length': String(fileBuffer.length),
      'x-request-id': ctx.requestId,
    },
  });
}
