/**
 * POST /api/customer/avatar
 *
 * Upload a new avatar image. Accepts multipart/form-data with a single
 * `avatar` file field. Validates magic bytes, resizes to 512x512 via
 * sharp, strips EXIF, converts to WebP, and stores on disk.
 */
export async function handleUploadAvatar(ctx: CustomerContext): Promise<NextResponse> {
  const { customer, db, now, request } = ctx;

  // --- 1. Parse multipart form data ---
  let formData: FormData;
  try {
    formData = await request.formData();
  } catch {
    return ctx.errorJson(
      'unsupported_media_type',
      'Request must be multipart/form-data with an avatar file.',
      415,
    );
  }

  const file = formData.get('avatar');
  if (file === null || typeof file === 'string') {
    return ctx.errorJson('validation_failed', 'Missing "avatar" file field.', 400);
  }

  // --- 2. Size check ---
  if (file.size > MAX_AVATAR_BYTES) {
    return ctx.errorJson(
      'payload_too_large',
      `Avatar file must be at most ${String(MAX_AVATAR_BYTES / 1024 / 1024)} MB.`,
      413,
    );
  }

  if (file.size === 0) {
    return ctx.errorJson('validation_failed', 'Avatar file is empty.', 400);
  }

  // --- 3. Read bytes and validate magic bytes ---
  const arrayBuffer = await file.arrayBuffer();
  const buffer = new Uint8Array(arrayBuffer);

  if (!isAllowedImageFormat(buffer)) {
    return ctx.errorJson(
      'validation_failed',
      'Only JPEG, PNG, and WebP images are allowed.',
      400,
    );
  }

  // --- 4. Process with sharp: strip EXIF, resize 512x512, convert to WebP ---
  const sharp = (await import('sharp')).default;
  let processedBuffer: Buffer;
  try {
    processedBuffer = await sharp(Buffer.from(arrayBuffer), SHARP_INPUT_GUARD)
      .rotate() // Auto-orient based on EXIF before stripping
      .resize(512, 512, { fit: 'cover', position: 'centre' })
      .removeAlpha()
      .webp({ quality: 85 })
      .toBuffer();
  } catch {
    return ctx.errorJson(
      'processing_failed',
      'Failed to process the image. Please try a different file.',
      422,
    );
  }

  // --- 5. Store to disk ---
  const { mkdir, writeFile, unlink } = await import('node:fs/promises');
  const path = await import('node:path');

  const basePath = getAvatarBasePath();
  await mkdir(basePath, { recursive: true });

  const newUuid = crypto.randomUUID();
  const filePath = path.join(basePath, `${newUuid}.webp`);
  await writeFile(filePath, processedBuffer);

  // --- 6. Delete old avatar file if exists ---
  const oldRows = await db
    .select({ avatarStorageKey: schema.customers.avatarStorageKey })
    .from(schema.customers)
    .where(eq(schema.customers.id, customer.id))
    .limit(1);

  const oldKey = oldRows[0]?.avatarStorageKey;
  if (oldKey !== null && oldKey !== undefined) {
    const oldPath = path.join(basePath, `${oldKey}.webp`);
    try {
      await unlink(oldPath);
    } catch {
      // Old file may already be deleted — ignore
    }
  }

  // --- 7. Update DB ---
  await db
    .update(schema.customers)
    .set({ avatarStorageKey: newUuid, updatedAt: now })
    .where(eq(schema.customers.id, customer.id));

  // --- 8. Audit ---
  const actor = customerActor({ id: customer.id, label: customerLabel(customer) });
  const target = uuidTarget({ kind: 'customer', id: customer.id });
  const auditCtx = buildAuditRequestContext({
    ip: ctx.ip,
    userAgent: ctx.userAgent,
    requestId: ctx.requestId,
  });

  await writeAudit(db, {
    action: 'customer.avatar_updated',
    actor,
    target,
    context: auditCtx,
    meta: { avatarStorageKey: newUuid },
    ts: now,
  });

  return ctx.json({ avatarUrl: `/api/customer/avatar/${newUuid}` }, 201);
}

